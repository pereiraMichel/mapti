        echo "  <div id='servicos' class='parallax'>";
        echo "      <div class='container'>";// id='fundoServicos'
//        echo "          <div class='row'>";
//        echo "              <div class='col-sm-12'>";
        echo "                  <h1 class='animated fadeInLeftBig' style='font-size: 30px;' id='corPretoAlpha'>";
        echo "                      SERV<span>IÇOS</span>";
        echo "                  </h1>";
        echo "                  <div class='wow fadeIn' data-wow-duration='1000ms' data-wow-delay='600ms'>";
        echo "                      <div class='row'>";

        switch ($s){
            default :
                $this->telaInicialServicos();
                break;
            case "mar" :
                $this->servMarketingDigital();
                break;
            case "sit" :
                $this->servSitesEcommerce();
                break;
            case "pro" :
                $this->servGestaoProjetos();
                break;
            case "ana" :
                $this->servAnaliseWeb();
                break;
            case "sis" :
                $this->servSistemas();
                break;
            case "sup" :
                $this->servSuporte();
                break;
        }
        
        echo "                      </div>";//row
        echo "                  </div>";//wow
        echo "      </div>";//container
        echo "  </div><!--/#Serviços-->";
    }
    
    
